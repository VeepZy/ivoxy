import type { Metadata, NextPage } from "next";
import { Inter } from "next/font/google";
import "./globals.css";
import { type ReactNode, Suspense } from "react";

import { getPlaylists } from "@/db/queries";
import { Menu } from "@/features/menu/components/menu";
import { PlayerProvider } from "@/features/player/components/context";
import { VideoPlayer } from "@/features/player/components/player";
import { Sidebar } from "@/features/sidebar/components/sidebar";

const FontSans = Inter({
    variable: "--font-sans",
    subsets: ["latin"],
    weight: "variable",
});

export const metadata: Metadata = {
    title: "Create Next App",
    description: "Generated by create next app",
};

const RootLayout: NextPage<Readonly<{ children: ReactNode }>> = async ({
    children,
}) => {
    const playlists = await getPlaylists();

    return (
        <html lang="en">
            <body
                className={`min-h-screen bg-background font-sans antialiased ${FontSans.variable}`}
            >
                <div className="relative flex min-h-screen flex-col">
                    <main className="flex-1">
                        <div className="hidden md:block">
                            <Menu />
                            <div className="border-t">
                                <div className="bg-background">
                                    <PlayerProvider>
                                        <div className="grid lg:grid-cols-5">
                                            <Sidebar
                                                playlists={playlists}
                                            />
                                            <div className="col-span-3 lg:col-span-4 lg:border-l">
                                                {children}
                                            </div>
                                            <Suspense fallback={null}>
                                                <VideoPlayer
                                                    playlists={playlists}
                                                />
                                            </Suspense>
                                        </div>
                                    </PlayerProvider>
                                </div>
                            </div>
                        </div>
                    </main>
                </div>
            </body>
        </html>
    );
};

export default RootLayout;
